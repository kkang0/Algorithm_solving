SELECT B.DEPT_ID, A.DEPT_NAME_EN, ROUND(AVG(B.SAL), 0) AVG_SAL
FROM HR_DEPARTMENT A JOIN HR_EMPLOYEES B
    ON A.DEPT_ID = B.DEPT_ID
GROUP BY B.DEPT_ID
ORDER BY AVG_SAL DESC;

















